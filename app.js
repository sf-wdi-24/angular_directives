var app = angular.module('sampleApp', []);

app.controller('MainCtrl', ['$scope', function ($scope) {
	$scope.test = "Welcome to the app!";

	$scope.pets = [
		{
			name: "Jasper",
			breed: "dog"
		},
		{
			name: "Doge",
			breed: "dog"
		},
		{
			name: "Sprinkles",
			breed: "cat"
		},
		{
			name: "Fluffy",
			breed: "unicorn"
		},
		{
			name: "Coconut",
			breed: "hamster"
		}
	];

	$scope.createPet = function() {
		$scope.pets.push($scope.newPet);
		$scope.newPet = {};
		$scope.newPetForm = false;
	};

	$scope.clickPet = function(pet) {
		pet.style = { 'color': 'red' };
	};

	$scope.deletePet = function(pet) {
		var petIndex = $scope.pets.indexOf(pet);
		$scope.pets.splice(petIndex, 1);
	};
}]);